#include "3-calc.h"
#include <stdio.h>

/**
* op_add - adds two integers.
*
* @a: fisrt number
* @b: second number
*
* Return: the sum of 2 int
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - substract two integers.
*
* @a: fisrt number
* @b: second number
*
* Return: The result of substraction of 2 int
*/

int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - multiply two integers.
*
* @a: fisrt number
* @b: second number
*
* Return: The result of multiplication of 2 int
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
* op_div - divide two integers.
*
* @a: fisrt number
* @b: second number
*
* Return: The result of division of a by b.
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
* op_mod - modulo of 2 int
*
* @a: fisrt number
* @b: second number
*
* Return: a modulo b
*/
int op_mod(int a, int b)
{
return (a % b);
}
